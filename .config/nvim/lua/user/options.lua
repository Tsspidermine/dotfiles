-- for help, go to :help options
vim.opt.number = true -- adds the numbers on the left
vim.opt.autoindent = true -- indents a newline based on what the current line is
vim.opt.smartindent = true -- indents lines based on filetype
vim.opt.smarttab = true -- makes tabs fancy
vim.opt.expandtab = true -- changes tabs into spaces
vim.opt.showmode = false -- disables things like the -- INSERT --
vim.opt.tabstop = 4 -- number of spaces that a tab stop is
vim.opt.softtabstop = -1 -- number of spaces that a tab stop is
vim.opt.shiftwidth = 4 -- number of spaces a tab is
vim.opt.mouse = "a" -- enable the mouse
vim.opt.backspace = "indent,eol,start" -- fancy backspaces
vim.opt.showtabline = 2 -- show tabs only when there are multiple
vim.opt.scrolloff = 8 -- the amount of lines that stay on screen above and below the cursor
vim.opt.sidescrolloff = 8 -- the amount of lines that stay on screen above and below the cursor
vim.opt.cursorline = false -- show a line where the cursor is
vim.opt.termguicolors = true -- enables more colors in the terminal
